let num1: number = 3;
let num2: number = 8;



//add num1 to num2 - and store into num1
num1 += num2; //this is shortcut to: num1= num1+num2;
document.write(`num1 += num2;  => num1=${num1} num2=${num2}<br/>`);


//sub num2 from num1 - and store into num1
num1 -= num2; //this is shortcut to: num1= num1-num2;
document.write(`num1 -= num2;  => num1=${num1} num2=${num2}<br/>`);

//mul num2 - and store into num2
num2 *= 2; //this is shortcut to: num2= num2*2;
document.write(`num2 *= 2;  => num1=${num1} num2=${num2}<br/>`);

//div num2 - and store into num2
num2 /= 2; //this is shortcut to: num2= num2/2;
document.write(`num2 /= 2;  => num1=${num1} num2=${num2}<br/>`);

//mod(7) of num1- and store into num1
num1 %= 7;//this is shortcut to: num1= num1%7;
document.write(`num1 %= 7;  => num1=${num1} num2=${num2}<br/>`);


